/**
 * add - addition
 * @a: first integer
 * @b: second integer
 * Return: a + b
 */
int add(int a, int b)
{
	return (a + b);
}


/**
 * sub - subtraction
 * @a: first integer
 * @b: second integer
 * Return: a - b
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiplication
 * @a: first integer
 * @b: second integer
 * Return: a * b
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - division
 * @a: first integer
 * @b: second integer
 * Return: a / b
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - modulus
 * @a: first integer
 * @b: second integer
 * Return: a % b
 */
int mod(int a, int b)
{
	return (a % b);
}
